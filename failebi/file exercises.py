# 1. დაწერეთ პროგრამა, რომლის მეშვეობით შექმნით ფაილს იმავე
# დირექტორიაში (საქაღალდეში), ჩაწერეთ მასში ნებისმიერი ტექსტი და
# დახურეთ ფაილი.
c = "ნებისმიერი ტექსტი და დახურეთ ფაილი."
with open("files.txt", "w", encoding="utf-8") as f:
    f.write(c)
# 2. დაწერეთ პროგრამა, რომლის მეშვეობით გახსნით ფაილს, წაიკითხავთ
# კონტენტს და დაბეჭდავთ ეკრანზე. დაითვალეთ სიმბოლოების რაოდენობა
