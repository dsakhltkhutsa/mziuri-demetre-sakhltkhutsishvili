# და ჩააკოპირებს მეორე (ახალ) ფაილში (შეამოწმეთ ქართულ უნიკოდზეც).
with open("files2.txt", "w", encoding="UTF-8") as f4:
       f4.write(a)
# 5. დაწერეთ პროგრამა, რომლის მეშვეობით, ორი ფაილის კონტენტს
